#include <stdio.h>
int main()
{
    char ch1,ch2,ch3;                  //3 character input variable
    ch1=65;
    ch2='b';
    ch3='3';
    printf("ch1=%c\n",ch1);         //print ascii value of ch1
    printf("ch2=%c\n",ch2);         //print same character
    printf("ch3=%c\n",ch3);         //print same character

    //using int datatype with char

    printf("ch1=%d\n",ch1);         //print as a int value
    printf("ch2=%d\n",ch2);         //print ascii value
    printf("ch3=%d\n",ch3);         //print ascii value
}
